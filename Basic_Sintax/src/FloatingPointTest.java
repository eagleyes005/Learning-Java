public class FloatingPointTest {

    public static void main(String[] args) {

        //para numeros com pontos flutuantes devemos usar double
        double salary = 1250.70;
        System.out.println("my salary is " + salary);

        double division = 3.14 / 2;
        System.out.println(division);

        int anotherDivision = 5 / 2;
        System.out.println(anotherDivision);


        double thirdDivision = 5.0 / 2;
        System.out.println(thirdDivision);

        double fourthDivision = 5.0 / 2;
        System.out.println(fourthDivision);
    }
}
