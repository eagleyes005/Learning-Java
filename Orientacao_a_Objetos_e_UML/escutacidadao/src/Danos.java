public class Danos {
    //Atributos
    private String nomeCidadao;
    private String telefone;
    private String tipoDeDano;
    private double prejuizo;

    //Construtor
    public Danos(String nomeCidadao, String telefone, String tipoDeDano, double prejuizo) {
        this.nomeCidadao = nomeCidadao;
        this.telefone = telefone;
        this.tipoDeDano = tipoDeDano;
        this.prejuizo = prejuizo;
    }
}
