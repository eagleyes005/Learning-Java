public class Endereco {
    //Atributos
    String bairro;
    String rua;
    int cep;
    int numero;

    //Construtor
    public Endereco(String bairro, String rua, int cep, int numero) {
        this.bairro = bairro;
        this.rua = rua;
        this.cep = cep;
        this.numero = numero;
    }
}
